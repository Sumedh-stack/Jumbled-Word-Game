    fun MixWords(word: String): String {
        val words = listOf(*word.split("".toRegex()).dropLastWhile { it.isEmpty() }.toTypedArray())
        Collections.shuffle(words)
        var mix = ""
        for (int in words)
            mix += int
        return mix
    }

